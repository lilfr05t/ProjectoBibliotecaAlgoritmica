    // MÉTODO QUE USA EL POLIMORFISMO Y LA HERENCIA para listar
    private void inicializarTabla() {
        // Define las columnas para la JTable
        modeloTablaLibros = new DefaultTableModel(
            new Object[]{"ISBN", "Título", "Tipo", "Disponible", "Autor"}, 0
        );
        tblLibros.setModel(modeloTablaLibros);
        cargarDatosTabla();
    }
    /**
     * Recorre la lista de libros del sistema y los muestra en la tabla.
     * Utiliza `instanceof` para determinar el tipo de libro y mostrarlo correctamente.
     */
    private void cargarDatosTabla() {
        modeloTablaLibros.setRowCount(0); // Limpiar filas
        
        for (Libro libro : biblioteca.gestorLibros.listarTodo()) {
            
            // Determina el tipo real del objeto (DEMOSTRACIÓN DE POLIMORFISMO en la vista)
            String tipo = (libro instanceof LibroTexto) ? "Texto" : "Investigación";
            
            modeloTablaLibros.addRow(new Object[]{
                libro.getISBN(), 
                libro.getTitulo(), 
                tipo,
                libro.isDisponible() ? "Sí" : "No",
                libro.getAutor()
            });
        }
    }
