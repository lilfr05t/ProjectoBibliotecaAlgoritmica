    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tblLibros.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un libro de la tabla para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String isbnEliminar = modeloTablaLibros.getValueAt(filaSeleccionada, 0).toString();

        try {
            // LLAMADA AL BACKEND: Buscar por ISBN usando el Predicate (Genéricos)
            Libro libroAEliminar = biblioteca.gestorLibros.buscarPorIdentificador(l -> l.getISBN().equals(isbnEliminar));
            
            if (libroAEliminar != null) {
                if (!libroAEliminar.isDisponible()) {
                    JOptionPane.showMessageDialog(this, "No se puede eliminar: el libro está actualmente prestado.", "Restricción", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                
                biblioteca.gestorLibros.eliminar(libroAEliminar); 
                cargarDatosTabla();
                JOptionPane.showMessageDialog(this, "Libro " + isbnEliminar + " eliminado y persistido.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                 JOptionPane.showMessageDialog(this, "Libro no encontrado en el sistema.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (PersistenciaException e) {
            JOptionPane.showMessageDialog(this, "Error de Persistencia: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String isbn = txtISBN.getText().trim();
        String titulo = txtTitulo.getText().trim();
        String autor = txtAutor.getText().trim();
        String tipo = cmbTipoLibro.getSelectedItem().toString(); 
        Libro nuevoLibro = null;
        
        try {
            if (isbn.isEmpty() || titulo.isEmpty() || autor.isEmpty()) {
                 JOptionPane.showMessageDialog(this, "Campos principales (ISBN, Título, Autor) incompletos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                 return;
            }
            
            // Creación polimórfica del objeto basada en la selección
            if (tipo.equals("Texto")) {
                int edicion = Integer.parseInt(txtEdicion.getText());
                nuevoLibro = new LibroTexto(isbn, titulo, autor, txtFacultad.getText(), edicion);
            } else if (tipo.equals("Investigación")) {
                nuevoLibro = new LibroInvestigacion(isbn, titulo, autor, txtTema.getText(), txtDOI.getText());
            }

            if (nuevoLibro != null) {
                // LLAMADA AL BACKEND: Usa Genéricos y Persistencia
                biblioteca.gestorLibros.agregar(nuevoLibro); 
                cargarDatosTabla(); // Actualiza la JTable
                JOptionPane.showMessageDialog(this, "Libro " + titulo + " guardado y persistido.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                // Limpiar campos
                limpiarCampos();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La edición debe ser un número válido.", "Error de Datos", JOptionPane.ERROR_MESSAGE);
        } catch (PersistenciaException e) {
            JOptionPane.showMessageDialog(this, "Error crítico de Persistencia: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
