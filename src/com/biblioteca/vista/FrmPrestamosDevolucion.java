    /**
     * Busca y muestra en la tabla los libros que un estudiante específico tiene prestados actualmente.
     * @param codigoEstudiante El código del estudiante a consultar.
     */
    private void cargarPrestamosActivos(String codigoEstudiante) {
        DefaultTableModel modelo = (DefaultTableModel) tblPrestamosActivos.getModel();
        modelo.setRowCount(0); // Limpiar filas

        try {
            // Se busca al estudiante usando el gestor genérico (Predicate)
            Estudiante estudiante = biblioteca.gestorEstudiantes.buscarPorIdentificador(e -> e.getCodigo().equals(codigoEstudiante));
            
            if (estudiante == null) {
                JOptionPane.showMessageDialog(this, "Estudiante no encontrado.", 
                        "Error de Búsqueda", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Llenar la tabla con los libros prestados
            for (Libro libro : estudiante.getLibrosPrestados()) {
                String tipo = (libro instanceof com.biblioteca.modelo.LibroTexto) ? "Texto" : "Investigación";
                modelo.addRow(new Object[]{
                    libro.getISBN(), 
                    libro.getTitulo(), 
                    tipo
                });
            }
            
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Error al cargar préstamos: " + e.getMessage(), 
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
