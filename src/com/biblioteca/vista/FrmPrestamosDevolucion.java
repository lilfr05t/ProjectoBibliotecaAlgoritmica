    private void btnPrestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrestarActionPerformed
       String codEstudiante = txtCodigoEstudiante.getText().trim();
        String isbn = txtISBNPrestamo.getText().trim();

        if (codEstudiante.isEmpty() || isbn.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el Código de Estudiante y el ISBN.", 
                    "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // LLAMADA AL SINGLETON para la lógica de negocio central
            biblioteca.realizarPrestamo(codEstudiante, isbn);
            
            // Si tiene éxito, recargamos la tabla del estudiante
            cargarPrestamosActivos(codEstudiante);
            
            // Limpieza de campos
            txtISBNPrestamo.setText("");

            JOptionPane.showMessageDialog(this, 
                    "Préstamo realizado exitosamente.\nEstudiante: " + codEstudiante + " | Libro: " + isbn,
                    "Éxito", JOptionPane.INFORMATION_MESSAGE);

        } catch (LimitePrestamosExcedidoException e) {
            // Captura de EXCEPCIÓN PERSONALIZADA: Límite de POO
            JOptionPane.showMessageDialog(this, "Error de Préstamo: " + e.getMessage(), 
                    "Límite Excedido", JOptionPane.ERROR_MESSAGE);
        } catch (RecursoNoEncontradoException e) {
            // Captura de EXCEPCIÓN PERSONALIZADA: Recurso no existe
            JOptionPane.showMessageDialog(this, "Error de Búsqueda: " + e.getMessage(), 
                    "Recurso No Encontrado", JOptionPane.WARNING_MESSAGE);
        } catch (PersistenciaException e) {
            // Captura de EXCEPCIÓN POR ARCHIVOS: Error de Serialización
            JOptionPane.showMessageDialog(this, "Error de Persistencia: " + e.getMessage(), 
                    "Error Grave", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPrestarActionPerformed

